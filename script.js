function initials(n){return n.split(' ').slice(0,2).map(w=>w[0]||'').join('').toUpperCase()}
function cleanPhone(phone){return phone.replace(/\s+/g,'')}
function telHref(phone){
  const ph=cleanPhone(phone);
  return phone.startsWith('+')?ph:'+91'+ph.replace(/^(91)/,'');
}
function escapeHtml(v){
  return String(v).replace(/[&<>"']/g,ch=>({'&':'&amp;','<':'&lt;','>':'&gt;','"':'&quot;',"'":'&#39;'}[ch]));
}
function photoUrl(url){
  return url+(url.includes('?')?'&':'?')+'sz=96';
}
async function copyPhone(phone,btn){
  try{
    await navigator.clipboard.writeText(cleanPhone(phone));
    const old=btn.innerHTML;
    btn.innerHTML='<i class="ti ti-check"></i> Copied';
    setTimeout(()=>btn.innerHTML=old,1200);
  }catch(e){
    window.prompt('Number copy करें:',cleanPhone(phone));
  }
}

