function buildAlpha(){
  const bar=document.getElementById('alpha-bar');
  const available=new Set(contacts.map(c=>c.name[0].toUpperCase()));
  'ABCDEFGHIJKLMNOPQRSTUVWXYZ'.split('').forEach(l=>{
    const b=document.createElement('button');
    b.className='a-btn'+(available.has(l)?'':' empty-letter');
    b.textContent=l;
    b.dataset.letter=l;
    b.addEventListener('click',()=>jumpToLetter(l));
    bar.appendChild(b);
  });
  const jumpFromPoint=e=>{
    const touch=e.touches&&e.touches[0]?e.touches[0]:e;
    const el=document.elementFromPoint(touch.clientX,touch.clientY);
    const btn=el&&el.closest?el.closest('.a-btn'):null;
    if(btn)jumpToLetter(btn.dataset.letter);
  };
  bar.addEventListener('touchstart',e=>{e.preventDefault();jumpFromPoint(e);},{passive:false});
  bar.addEventListener('touchmove',e=>{e.preventDefault();jumpFromPoint(e);},{passive:false});
  bar.addEventListener('mousemove',e=>{if(e.buttons===1)jumpFromPoint(e);});
}

