function jumpToLetter(letter){
  document.querySelectorAll('.a-btn').forEach(x=>x.classList.toggle('active',x.dataset.letter===letter));
  const section=document.querySelector(`.section[data-letter="${letter}"]`)||document.querySelector(`.card[data-letter="${letter}"]`);
  showLetterPop(letter);
  if(!section)return;
  if(section.classList.contains('section'))section.classList.add('open');
  openLetters.add(letter);
  section.scrollIntoView({behavior:'auto',block:'start'});
}

function render(q='',alpha=null){
  const list=document.getElementById('list');list.innerHTML='';
  const lq=q.toLowerCase();
  let f=contacts.filter(c=>c.name.toLowerCase().includes(lq)||c.phone.includes(lq));
  if(alpha)f=f.filter(c=>c.name[0].toUpperCase()===alpha);
  document.getElementById('cnt').textContent=f.length+' contact'+(f.length!==1?'s':'')+' found';
  if(!f.length){list.innerHTML='<div class="empty"><i class="ti ti-search-off" style="font-size:36px;display:block;margin-bottom:8px"></i>कोई contact नहीं मिला</div>';return;}
  if(!features.groups){
    f.forEach(c=>list.appendChild(makeCard(c)));
    return;
  }
  const grouped=f.reduce((acc,c)=>{const l=c.name[0].toUpperCase();(acc[l]||(acc[l]=[])).push(c);return acc;},{});
  Object.keys(grouped).sort().forEach(l=>{
    const section=document.createElement('section');
    section.className='section'+(openLetters.has(l)?' open':'');
    section.dataset.letter=l;
    const header=document.createElement('button');
    header.className='sec-hdr';
    header.type='button';
    header.innerHTML=`<span class="sec-title">${l}</span><span class="sec-meta">${grouped[l].length} contacts <i class="ti ti-chevron-down"></i></span>`;
    const body=document.createElement('div');
    body.className='sec-body';
    grouped[l].forEach(c=>body.appendChild(makeCard(c)));
    header.addEventListener('click',()=>{
      section.classList.toggle('open');
      if(section.classList.contains('open'))openLetters.add(l);else openLetters.delete(l);
    });
    section.append(header,body);
    list.appendChild(section);
  });
}

